    private void tblAppoint_init(){
        tblAppoint.getTableHeader().setFont(new Font("Calibri", Font.BOLD, 16));
        TableColumnModel appointCols = tblAppoint.getColumnModel();
        if(appointCols.getColumnCount() < 4){
            appointCols.addColumn(appointIdCol);
        }
        TableColumn consultCol = tblAppoint.getColumnModel().getColumn(2);
        
        JComboBox cbxAppointCon = new JComboBox();
        ComboBoxTableCellRenderer r = new ComboBoxTableCellRenderer();
        
        cbxManAppointCon_init(cbxAppointCon); // add consultants to Combo Box
        r.setModel(cbxAppointCon.getModel());
        cbxAppointCon.addItemListener(new java.awt.event.ItemListener(){
            @Override
            public void itemStateChanged(java.awt.event.ItemEvent e){
                if(e.getStateChange() == ItemEvent.SELECTED){
                    //System.out.println(e.getItem().toString());
                    appointSelectCon(tblAppoint.getSelectedRow(), cbxAppointCon);
                }  
            }
        });
        // BUG
        //consultCol.setCellRenderer(r); // Set Cell view to Combo Box
        consultCol.setCellEditor(new DefaultCellEditor(cbxAppointCon)); // Set Cell editor as Combo Box
        appointCols.removeColumn(appointIdCol);

    }
    private void tblAppointFill(){
        ArrayList<Appointment> appList = FMCdb.getAppointments();
        TableColumnModel appointCols = tblAppoint.getColumnModel();
        if(appointCols.getColumnCount() < 4){
            appointCols.addColumn(appointIdCol);
        }
        DefaultTableModel model = (DefaultTableModel) tblAppoint.getModel();
        model.setRowCount(0);
        for(int i = 0; i < appList.size(); i++){
            if(appList.get(i).getConsultant_id() != 0){
                Consultant con = FMCdb.getConsultantById(appList.get(i).getConsultant_id());
                Client client = FMCdb.getClientById(appList.get(i).getClient_id());

                Object[] o = {appList.get(i).getDate(), client.getFirst_name() + " "
                        + client.getLast_name(), con.cbxString(), appList.get(i).getId()};
                model.addRow(o);
            }
        }
        
         // remove ID column from view
        
        tblAppoint.setModel(model);
        appointCols.removeColumn(appointIdCol);
        
        //System.out.println("Value of COL 1 ROW 1: " + tblAppoint.getModel().getValueAt(0, 0));
                
    }
    private void appointSelectCon(int row, JComboBox cbx){
        
        DefaultTableModel model = (DefaultTableModel) tblAppoint.getModel();
        if(row >= 0 && Validate.isCbxValid(cbx, "Select a Consultant...")){
            tblAppoint.setValueAt(cbx.getSelectedItem(), tblAppoint.getSelectedRow(), 2);
            String selected = tblAppoint.getValueAt(row, 2).toString();
            
            int id = Integer.parseInt(selected.split(" ")[0]);            
            int appId = Integer.parseInt(model.getValueAt(row, 3).toString());
            
            Appointment appoint = FMCdb.getAppointmentById(appId);
            appoint.setConsultant_id(id);
            /*System.out.println("READY FOR DATABASE\n");
            System.out.println("ID: " + model.getValueAt(row, 3) +
                    " | DATE: " + model.getValueAt(row, 0) +
                    " | CLIENT: " + model.getValueAt(row, 1)  +
                    " | CONSULTANT ID: " + id);*/
           
            try{
                FMCdb.updateAppointmentById(appoint);
                Message.okPopup("Appointment successfully UPDATED in database.");
                tblAppointFill();
            }catch(Exception e){
               if(DEBUG) e.printStackTrace();
            }
        }    
    }
