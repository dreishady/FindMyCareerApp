    private String getSliderDemand(JSlider s){
        String d;
        switch(s.getValue()){
            case 0:
                d = "Low";
                break;
            case 1:
                d = "Medium";
                break;
            case 2:
                d = "High";   
                break;
            default:
                d = "Low";
                break;
        }
        return d;
    }
