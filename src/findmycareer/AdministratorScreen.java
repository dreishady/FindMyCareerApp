    private void removeConClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeConClick
        // TODO add your handling code here:
        if(Validate.isCbxValid(cbxConsultants, "Select a Consultant...")){
            int id = cbxGetID(cbxConsultants);
            ArrayList<Appointment> apppointList = FMCdb.getAppointmentsByConsultantId(id);
            ArrayList<Career> careerList = FMCdb.getCareersByConsultantId(id);
            if(apppointList.isEmpty() && careerList.isEmpty()){
                try{
                    FMCdb.deleteConsultantById(id);
                    clearManConForm();
                    cbxConultant_init(cbxConsultants);
                    Message.okPopup("Consultant successfully REMOVED from database.");
                }catch(Exception e){
                    if(DEBUG) e.printStackTrace();
                }
            }else{
                Message.warnPopup("Consultant in use.");
            }
        } 
    }//GEN-LAST:event_removeConClick
