    class ProgressBarUpdate extends SwingWorker<Void, Void>{

        @Override
        protected Void doInBackground(){
            SQLreader.create(dbProgress, txtDbName.getText(), txtDbUser.getText(), txtDbPass.getText());
            return null;  
        }
        
        @Override
        public void done(){
            closeWindow();
        }
        
    }
    
    public void closeWindow(){
        adminScreen.setEnabled(true);
        String oldUser = Settings.getSetting("username");
        String oldPassword = Settings.getSetting("password");
        String server = Settings.getSetting("server");
        String port = Settings.getSetting("port");
        //System.out.println("DATABASE RENAME: " + txtDbName.getText());
        Settings.setSetting("db", txtDbName.getText());
        Settings.setSetting("url_db", "jdbc:mysql://" + server + ":" + port + "/" + txtDbName.getText());
        Settings.setSetting("url", "jdbc:mysql://" + server + ":" + port);
        FMCdb.changeDbuser(oldUser, oldPassword, oldUser);
        

        this.setVisible(false);
        //Message.okPopup("<html><center>Database reset complete!</center><br><br><center> Program must be restared.</center></html>");
        Message.okPopup("<html>Database rebuild complete!</html>");
        adminScreen.setVisible(true);
        this.dispose();  
    }
