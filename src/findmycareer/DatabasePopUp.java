    private void submitClick(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitClick
        // TODO add your handling code here:
        if(Validate.isValidString(txtDbName) && Validate.isValidUsername(txtDbUser) && Validate.isValidDbPassword(txtDbPass, txtDbVerify)){
            if(Validate.isValidConnection(txtDbUser.getText(), txtDbPass.getText())){
                this.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
                lblWait.setVisible(true);
                dbProgress.setVisible(true);
                txtDbName.setVisible(false);
                txtDbUser.setVisible(false);
                txtDbPass.setVisible(false);
                txtDbVerify.setVisible(false);
                dbProgress.setMaximum(SQLreader.countCodeLines());
                btnBack.setVisible(false);
                btnSubmit.setVisible(false);
                lblName.setVisible(false);
                lblUser.setVisible(false);
                lblPass.setVisible(false);
                lblVerify.setVisible(false);
                
                ProgressBarUpdate task = new ProgressBarUpdate();
                task.execute();  
                
            }else{
                Message.error("<html><b>Database connection could not be made.</b><br>"
                        + "<br>Current server: " + Settings.getSetting("server")
                        + "<br>Current PORT : " +  Settings.getSetting("port")
                        + "</html>");
            }
        } 
    }//GEN-LAST:event_submitClick
 
